   <p>Read the profiles below to learn about the countries you can visit on a World Tours vacation.</p>
   <hr />
   <h2><img src="images/etosha.jpg" width="250" height="165" align="right" />Namibia</h2>
   <p><strong>Region</strong>: 
	Africa</p>
   <p><strong>Description</strong>:<br />
	Formerly South-West Africa, Namibia's boundaries reach from the Atlantic Ocean to the Kalahari Desert. One of its great claims to fame is the unparalleled Etosha National Park, home to leopards, antelope, giraffes, elephants, flamingos, and rhinoceroses. </p>
   <p><strong>Image Caption</strong>: 
	Etosha National Park, Namibia</p>
   <p><strong>Population</strong>: 
	1,622,328</p>
   <p><strong>Local Currency</strong>: 
	Namibian Dollar</p>
   <hr />
   <h2><img src="images/wailing_wall2.jpg" width="250" height="165" align="right" />Israel</h2>
   <p> <strong>Region</strong>: 
	Asia, West</p>
   <p><strong>Description</strong>:<br />
	A state whose borders enclose some of the holiest sites of three of the world's major religions&mdash;Christianity, Islam, and Judaism&mdash;Israel is also a rugged land, ideal for hiking and exploration. </p>
   <p><strong>Image Caption</strong>: 
	The Wailing Wall, Jerusalem</p>
   <p><strong>Population</strong>: 
	5,643,966</p>
   <p><strong>Local Currency</strong>: 
	New Israeli Shekel</p>
   <hr />
   <h2><img src="images/dc.jpg" width="250" height="165" align="right" />United States</h2>
   <p><strong>Region</strong>: 
	North America</p>
   <p><strong>Description</strong>:<br />
	Situated between Canada and Mexico in North America, the United States is a large country with diverse topography and culture. Settled initially by Europeans, the United States has become a &quot;melting pot&quot; of races and cultures, resulting in a unique culture and style. </p>
   <p><strong>Image Caption</strong>: 
	Washington, D.C., with the Washington Monument in the background</p>
   <p><strong>Population</strong>: 
	270,311,758</p>
   <p><strong>Local Currency</strong>: 
	U.S. Dollar</p>
   <hr />
   <h2><img src="images/miyajima.jpg" width="250" height="165" align="right" />Japan</h2>
   <p> <strong>Region</strong>: Asia,
East</p>
   <p><strong>Description</strong>:<br />
	A country composed of islands in East Asia, Japan is home to a rich and diverse culture, and majestic mountains. Nearly 80 percent of the country is covered in mountains, many of which are protected in an extensive national park system. </p>
   <p><strong>Image Caption</strong>: 
	Island of Miyajima, Japan</p>
   <p><strong>Population</strong>: 
	125,931,533</p>
   <p><strong>Local Currency</strong>: 
	Yen</p>
   <hr />
   <h2><img src="images/paris.jpg" width="165" height="250" align="right" />France</h2>
   <p> <strong>Region</strong>: Europe</p>
   <p><strong>Description</strong>:<br />
	From Paris street cafes to moutain chalets in the Alps and casinos in the French Riviera, France is a classic touring destination. Hike in the mountains, lounge  on a Mediterranean beach, or look down on Paris from a perch on the Eiffel Tower. </p>
   <p><strong>Image Caption</strong>: 
	Paris at dawn</p>
   <p><strong>Population</strong>: 
	58,804,944</p>
   <p><strong>Local Currency</strong>: 
	Euro</p>
   <hr />
   <h2><img src="images/stonehenge.jpg" width="250" height="165" align="right" />United Kingdom</h2>
   <p> <strong>Region</strong>: Europe</p>
   <p><strong>Description</strong>:<br />
	A nation comprising England, Wales, Scotland, and Northern Ireland, the United Kingdom is a popular spot for tourists. Whether you seek the bustle of modern London or the medieval charm of the towns and villages sprinkled abundantly throughout the countryside, the United Kingdom is rich with historical sites to see. </p>
   <p><strong>Image Caption</strong>: 
	England's enigmatic Stonehenge</p>
   <p><strong>Population</strong>: 
	58,970,119</p>
   <p><strong>Local Currency</strong>: 
	Pound Sterling</p>
   <hr />
