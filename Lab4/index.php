   <h3><img src="images/icon_tj.gif" width="81" height="81" align="right" />Teens Discover the Mayans</h3>
<p><strong>By Ellen Olenska</strong></p>
   <p>World Tours recently took two dozen students of Bart Township High School to Chichen Itza, Mexico. While on the tour, students saw ancient Mayan ruins and artifacts. They learned the latest on the &quot;mystery of the Mayans.&quot;</p>
   <p>The Mayans were one of the world's most advanced civilizations when they mysteriously disappeared centuries ago. While archaeologists don't know exactly what happened to the Mayans, evidence points to war as one possible explanation. Bart Township High School students are using the experience as the basis for a series of reports that teams of  participants are putting together.</p>

