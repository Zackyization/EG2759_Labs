	<h1><br />
	About World Tours</h1>
    <p>Since its founding in 1968 by Archer and May World, hiking enthusiasts with a taste for exotic culture and scenery, World Tours has offered travel tours for a variety of interests and activity levels. </p>
    <p><img src="images/french_alps.jpg" width="169" height="250" align="left" alt="This photo of the French Alps was taken on a recent hiking tour to the area." />In the late 1960s, World Tours specialized exclusively in travel tours for those leading active lifestyles, which were usually led by Archer or May World themselves. While many of our tours still emphasize activity&mdash;including hiking, kayaking, inline skating, and bicycling&mdash;most of our tours are suitable for the whole family.</p>
   <p>Today, our tours are still led by experienced traveling enthusiasts, whose knowledge of the locations is outpaced only by their passion for sharing it.</p>
